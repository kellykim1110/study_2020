char* solution(const char* s) {
    // return 값은 malloc 등 동적 할당을 사용해주세요. 할당 길이는 상황에 맞게 변경해주세요.
    char* answer;
    int len = strlen(s);

    // 문자열의 개수가 짝수일 때  가운데 두글자
    if (len % 2 == 0) {
        answer = (char*)malloc(2);

        answer[0] = s[(len / 2) - 1];
        answer[1] = s[len / 2];
        answer[2] = '\0';
    }
    // 문자열의 개수가 홀수일 때, 가운데 한글자
    else {
        answer = (char*)malloc(1);
        answer[0] = s[len / 2];
        answer[1] = '\0';
    }

    return answer;
}