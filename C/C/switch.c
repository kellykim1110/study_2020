#include <stdio.h>
int main() {
	int input;
	printf("강아지 키우기\n");
	printf("무엇을 하실래요? \n");
	printf("1. 밥주기\n");
	printf("2. 씻기기\n");
	printf("3. 재우기\n");

	scanf("%d", &input);

	if (input == 1) {
		printf("아이 맛있어. \n");
	}
	else if (input == 2) {
		printf("아이 시원해. \n");
	}
	else if (input == 3) {
		printf("zzz  \n");
	}
	else {
		printf("무슨 말이에여??? 왈왈\n");
	}

	return 0;
}

